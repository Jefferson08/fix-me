    private void btnAlterarChamado1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarChamado1ActionPerformed

        dadosChamado.setUrgencia(jcbUrgencia.getSelectedItem().toString());
        dadosChamado.setImpacto(jcbImpacto.getSelectedItem().toString());
        dadosChamado.setPrioridade(Integer.toString(verificaPrioridade()));

        if (jcbStatus.getSelectedIndex() == 0) {
            dadosChamado.setStatus("1");
        } else if (jcbStatus.getSelectedIndex() == 1) {
            dadosChamado.setStatus("2");
        } else if (jcbStatus.getSelectedIndex() == 2) {
            dadosChamado.setStatus("3");
        }
        

        try {
            ChamadoDAO.alteraChamado(dadosChamado);
            
            frameAlterarChamado.setVisible(false);
            frameGerenciarChamados.setVisible(true);
            
            carregaTabelaChamados();
            

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        }
    }//GEN-LAST:event_btnAlterarChamado1ActionPerformed

