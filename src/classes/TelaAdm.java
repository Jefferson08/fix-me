    public int verificaPrioridade(){
        int tmpPrioridade = 0;
        
        if (dadosChamado.getImpacto() == "Alto" && dadosChamado.getUrgencia() == "Alta") {
            tmpPrioridade = 1;
        }else if(dadosChamado.getImpacto() == "Alto" && dadosChamado.getUrgencia() == "Média"){
            tmpPrioridade = 2;
        }else if(dadosChamado.getImpacto() == "Alto" && dadosChamado.getUrgencia() == "Baixa"){
            tmpPrioridade = 3;
        }else if(dadosChamado.getImpacto() == "Médio" && dadosChamado.getUrgencia() == "Alta"){
            tmpPrioridade = 2;
        }else if(dadosChamado.getImpacto() == "Médio" && dadosChamado.getUrgencia() == "Média"){
            tmpPrioridade = 3;
        }else if(dadosChamado.getImpacto() == "Médio" && dadosChamado.getUrgencia() == "Baixa"){
            tmpPrioridade = 4;
        }else if(dadosChamado.getImpacto() == "Baixo" && dadosChamado.getUrgencia() == "Alta"){
            tmpPrioridade = 3;
        }else if(dadosChamado.getImpacto() == "Baixo" && dadosChamado.getUrgencia() == "Média"){
            tmpPrioridade = 4;
        }else if(dadosChamado.getImpacto() == "Baixo" && dadosChamado.getUrgencia() == "Baixa"){
            tmpPrioridade = 5;
        }
        
        
        return tmpPrioridade;
    }

    public void carregaTabelaChamados() {

        while (mdlChamados.getRowCount() > 0) {
            mdlChamados.removeRow(0);
        }

        try {
            List<Chamado> vetorChamado = new ArrayList<Chamado>();

            vetorChamado = ChamadoDAO.listaChamadosAdm();

            for (Chamado chamadoAtual : vetorChamado) {
                String dadosChamado[] = new String[7];

                dadosChamado[0] = chamadoAtual.getID();
                dadosChamado[1] = chamadoAtual.getSubCategoria();
                dadosChamado[2] = chamadoAtual.getData();
                dadosChamado[3] = chamadoAtual.getUrgencia();
                dadosChamado[4] = chamadoAtual.getImpacto();
                dadosChamado[5] = chamadoAtual.getPrioridade();
                dadosChamado[6] = chamadoAtual.getStatus();

                mdlChamados.addRow(dadosChamado);

            }

        } catch (Exception erro) {
            JOptionPane.showMessageDialog(null, erro.getMessage());
        }
    }
    
